package day08_relational_logical_operators;

public class GiftCard {

    public static void main(String[] args) {

        double balance = 200;
        System.out.println("Gift card balance is: " + balance);

        System.out.println("Buy item 1 and cost is $40");

        balance -= 40;
        System.out.println("Gift card balance is: " + balance);
        System.out.println("Buy item 2 and cost is $100");

        balance -= 100;
        System.out.println("Gift card balance is: " + balance);





    }



}
