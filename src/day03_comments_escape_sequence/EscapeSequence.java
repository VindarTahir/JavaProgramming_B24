package day03_comments_escape_sequence;

public class EscapeSequence {

    public static void main(String[] args) {

        System.out.println("I love \"java\" programming"); //Here we use \" to put quotations around java

        System.out.println("I like to \'cook\'\\eat"); // This puts a single quote around cook

        System.out.println("Tomorrow is Friday,\nIt is a day off"); // The \n makes the letters go to the next line

        System.out.println("\tMessage"); // \t will give a tab in the output ( 5 spaces )

    }


}
