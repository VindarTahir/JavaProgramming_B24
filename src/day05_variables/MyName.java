package day05_variables;

public class MyName {

    public static void main(String[] args) {


        char letterOne = 'V';
        char letterTwo = 'i';
        char letterThree = 'n';
        char letterFour = 'd';
        char letterFive = 'a';
        char letterSix = 'r';

        System.out.println("My name is " + letterOne + letterTwo + letterThree + letterFour + letterFive + letterSix);

        // If a string is not in beginning, will just give number values as output due to ASCII table

        System.out.print(letterOne);
        System.out.print(letterTwo);
        System.out.print(letterThree);
        System.out.print(letterFour);
        System.out.print(letterFive);
        System.out.print(letterSix + "\n");
        /*
        System.out.println(letterOne + letterTwo + etc.);
        Above adds the number values together
         */


        char numOne = 86;
        char numTwo = 105;
        char numThree = 110;
        char numFour = 100;
        char numFive = 97;
        char numSix = 114;

        System.out.print(numOne);
        System.out.print(numTwo);
        System.out.print(numThree);
        System.out.print(numFour);
        System.out.print(numFive);
        System.out.print(numSix);








    }


}
